using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Session03
{
    internal class TinhToan
    {
        public static int TinhTong(int x, int y)
        {
            return x + y;
        }

        public static int TinhHieu(int x, int y)
        {
            return x - y;
        }

        public static int TinhTich(int x, int y)
        {
            return x * y;
        }

        public static int TinhThuong(int x, int y)
        {
            return x / y;
        }
    }
}
